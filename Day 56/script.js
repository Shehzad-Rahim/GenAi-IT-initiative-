// Type Conversion in javascript

//String Conversion
let value = true;
alert(value);

value = String(value); // value becoms string "true";

//Numeric Conversion
let strToNum = '10'
alert(strToNum);

strToNum = Number(strToNum); // strToNum becomes number 10;

let strToBool = 'Yes';
strToBool = Boolean(strToBool); // strToBool becomes boolean true
strToBool = '';
strToBool = Boolean(strToBool); // strToBool becomes boolean false