"use strict";
function usingCompoundoperators() {
    let a = 5;
    console.log("Initial a: ", a);
    a += 3;
    console.log("After Addition", a);
    a -= 2;
    console.log("After Subtraction", a);
    a *= 4;
    console.log("After Multiplication", a);
    a /= 2;
    console.log("After Division", a);
}
;
usingCompoundoperators();
